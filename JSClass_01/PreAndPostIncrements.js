/*
1: postfix Increment and Decrement:

Follow two steps
                  first: use the value 
                  second: increment or decrement the value

                  e.g
                  var a = 5;
                  console.log(a++);
                  console.log(a);
                  
                   var a = 5;
                  console.log(a--);
                  console.log(a);

2: prefix  Increment and Decrement

Follow two steps
                  first : increment or decrement the value  
                  second : use the value
                   e.g
                  var a = 5;
                  console.log(++a);
                  console.log(a);   

                     var a = 5;
                  console.log(--a);
                  console.log(a);   

                  */